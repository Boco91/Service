    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql="SELECT * FROM Korisnik where Korisnicko_ime=? and Lozinka=?";
        try{
            pst= conn.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jPasswordField1.getText());
            rs = pst.executeQuery();
            if(rs.next()){
                rs.close();
                pst.close();
                
                setVisible(false);
                Ucitavanje uc = new Ucitavanje();
                uc.PUcitavanje();
                uc.setVisible(true);
            }else{
            JOptionPane.showMessageDialog(null, "Pogresno ste unijeli korisnicko ime ili sifru", "UPOZORENJE" ,JOptionPane.INFORMATION_MESSAGE);
            }
        
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
        }finally{
        try{
        rs.close();
        pst.close();
        
        }catch(Exception e){}
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

